# a, b = 12, 3
# a, b = 7675, 179
# =============================================================================

a, b = [int(i) for i in input().split()]

x, y = max(a, b), min(a, b)
toadd = ""

print(x, "*", y)
while y > 0 :
    if (y % 2) == 1 :
            
        toadd = toadd + " + " + str(x)
        y -= 1
        
    elif (y % 2) == 0 :
        
        x *= 2
        y = int(y/2)
        
    print("=", x, "*", str(y) + toadd)
        
print("= " + str(a*b))
